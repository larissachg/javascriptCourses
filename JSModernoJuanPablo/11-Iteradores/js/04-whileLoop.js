// Un while se va a ejecutar mientras una condicion sea verdadera

let i = 0; // Inicializar el while

// while(Aqui se coloca la condicion que se va a realizar){ Condicion }
while(i < 10){
    console.log(`Numero ${i}`);

    i++; // Incremento
}


